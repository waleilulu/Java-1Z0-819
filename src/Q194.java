public class Q194 {
    private int x;
    private static int y;
    public static void main(String[] args) {
        Q194 t1 = new Q194(); // 建立實例 t1，其 x 初始化為 0（默認值）。
        t1.x = 2; // 設置 t1.x 的值為 2。
        Q194.y = 3; // 設置靜態變數 y 的值為 3。

        Q194 t2 = new Q194(); // 建立實例 t2，其 x 初始化為 0。
        t2.x = 4; // 設置 t2.x 的值為 4。
        t2.y = 5; // 雖然是通過 t2 修改的，但 y 是靜態變數，會影響所有實例。此時 y 的值變為 5。

        System.out.println(t1.x + "," + t1.y); // t1.x = 2 , t1.y 是靜態變數，值為 5。
        System.out.println(t2.x + "," + Q194.y); // t2.x = 4 , Q194.y 是靜態變數，值為 5。
        System.out.println(t2.x + "," + t1.y); // t2.x = 4 , t1.y 是靜態變數，值為 5（靜態變數 y 是共享的，無論使用哪個實例訪問，值都是一樣的）。
    }
}
/*
靜態變數共享：

靜態變數是類別層級的變數，所有實例共用同一個變數。
通過類別名稱或任何實例名稱修改靜態變數，其值都會影響到其他實例。
實例變數獨立：

每個實例的實例變數是獨立的，不會相互影響。
靜態變數的存取建議：

雖然可以通過實例名稱訪問靜態變數，但建議直接使用類別名稱訪問，這樣可以避免混淆。

靜態變數 y 的值是所有實例共享的，最後的值為 5。
每個實例的 x 值是獨立的，t1.x 為 2，t2.x 為 4。
*/

